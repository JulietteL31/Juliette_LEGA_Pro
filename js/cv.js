//fonction ouverture 1
let fleche = document.getElementById('fleche');
let description = document.getElementById('descriptionOuvre')
let ouvre1 = document.querySelector('.item-index1')

function Ouvre() {
    if(getComputedStyle(description).display === "none") {
        fleche.className = 'ouvre'
        fleche.style.cursor = "pointer"
        description.className = 'descriptionOuvre'
    } else {
        fleche.className = 'ferme'
        fleche.style.cursor = "pointer"
        description.className = "descriptionFerme"
    }
}

ouvre1.addEventListener('click', Ouvre);

